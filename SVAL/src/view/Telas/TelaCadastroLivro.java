    private void jButton4MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MousePressed
        // TODO add your handling code here:
        if (pegarId() == -1) {
            JOptionPane.showMessageDialog(null, "Por favor, selecione um registro para alterar!");
        } else {
            Long id = Long.parseLong(txtCodigo.getText());
            String titulo = txtTitulo.getText();
            String autor = txtAutor.getText();
            String edicao = txtEdicao.getText();
            Integer sbn = Integer.parseInt(txtSbn.getText());
            Integer quantidade = Integer.parseInt(txtQuantidade.getText());
            Double preco = Double.parseDouble(txtPreco.getText());
            Date date = jdcAno.getDate();
            LocalDate ano = date.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            if (LivroController.actualizar(id, titulo, autor, edicao, sbn, ano, preco, quantidade)) {
                JOptionPane.showMessageDialog(null, "Dados do livro actualizados!");
                limparCampos();
                preencherTabela();
            }

        }
    }//GEN-LAST:event_jButton4MousePressed

    private void btnExcluirMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMousePressed
        // TODO add your handling code here:
        if (pegarId() == -1) {
            JOptionPane.showMessageDialog(null, "Por favor, selecione um registro para excluir!");
        } else {
            if (LivroController.eliminar(Long.parseLong(txtCodigo.getText()))) {
                JOptionPane.showMessageDialog(null, "Livro excluido!");
                limparCampos();
                preencherTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao tentar eliminar o livro");
            }
        }
    }//GEN-LAST:event_btnExcluirMousePressed


