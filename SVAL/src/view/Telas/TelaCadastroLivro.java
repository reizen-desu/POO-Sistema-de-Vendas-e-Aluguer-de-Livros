    public Long pegarId() {
        //pegando o numero da linha selecionada
        int linhaSelecionada = tblLivros.getSelectedRow();
        //caso nenhuma linha seja selecionada
        if (linhaSelecionada == -1) {

        } else {
            //pegando o primeiro valor da linha seleciona que eh o ID do usuario
            Long id = Long.parseLong(tblLivros.getValueAt(linhaSelecionada, 0).toString());
            return id;
        }
        return -1l;
    }

    private void preencherCampos(Long id) {
        controller = new LivroJpaController(ConnectionFactory.getemf());
        livro = controller.findLivro(id);

        txtCodigo.setText(String.valueOf(id));
        txtAutor.setText(livro.getAutor());
        txtEdicao.setText(livro.getEdicao());
        txtQuantidade.setText(String.valueOf(livro.getQuantidade()));
        txtPreco.setText(String.valueOf(livro.getPreco()));
        txtSbn.setText(String.valueOf(livro.getSbn()));
        txtTitulo.setText(livro.getTitulo());

    }

