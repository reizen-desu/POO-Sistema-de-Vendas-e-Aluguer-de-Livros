    private void preencherTabela() {
        controller = new LivroJpaController(ConnectionFactory.getemf());
        livros = controller.findLivroEntities();

        DefaultTableModel tabela = (DefaultTableModel) tblLivros.getModel();
        tabela.setNumRows(0);

        for (Livro livro : livros) {
            Object[] obj = {
                livro.getId(),
                livro.getTitulo(),
                livro.getAutor(),
                livro.getEdicao(),
                livro.getSbn(),
                livro.getAno(),
                livro.getQuantidade(),
                livro.getPreco(),
                livro.getStatus()
            };
            tabela.addRow(obj);
        }

    }

